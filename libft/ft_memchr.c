#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t					i;
	const unsigned char		*puc;

	i = 0;
	if (!(puc = ((const unsigned char *)s)))
		return (NULL);
	while (i < n)
	{
		if (puc[i] == (unsigned char)c)
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
